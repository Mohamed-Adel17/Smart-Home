#include "STD_TYPES.h"
#include "DIO_interface.h"
#include "Lights.h"

/**********************************************************************************************************************
 *  Functions Definitions
 *********************************************************************************************************************/
 
void LIGHT_Control(u16 LDR_DATA)
{

	u8 Duty = ((LDR_DATA/1024.0) * 100);

	if((Duty>=25))
	{
		DIO_SetPinValue(LIGHT_PORT,LIGHT_PIN,_LOW);
	}else
	{
		DIO_SetPinValue(LIGHT_PORT,LIGHT_PIN,_HIGH);
	}

}


	
